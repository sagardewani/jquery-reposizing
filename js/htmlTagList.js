var shape = {
	square:function(element,area,scale){
		if(area === 'undefined')area=50;
		if(scale == undefined)scale ="px";
		element.width(area);
		element.height(area);
		element.css('border-radius','0px');
		return "square";
	},
	rectangle:function(element,width,height,scale){
		if(width === 'undefined')width=40;
		if(height === 'undefined')height=30;
		if(scale == undefined)scale ="px";
		element.width(width);
		element.height(height);
		element.css('max-width',(width)+scale);
		element.css('max-height',(height)+scale);
		element.css('min-width',(width/2)+scale);
		element.css('min-height',(height/2)+scale);
		element.css('border-radius','0px');
		return "rect";
	},
	circle:function(element,radius,b_rx,b_ry,scale){
		if(b_rx == undefined)b_rx="100%";
		if(b_ry == undefined)b_ry=b_rx;
		if(scale == undefined)scale ="px";
		element.css('max-width',(2*radius)+scale);
		element.css('max-height',(2*radius)+scale);
		element.css('height',(radius)+scale);
		element.css('width',(radius)+scale);
		element.css('min-width',(radius)+scale);
		element.css('min-height',(radius)+scale);
		var array = ['-webkit-','-moz-',''];
		for(i=0;i<array.length;i++)
		{
			element.css(array[i]+'border-radius',b_rx);
		}
		return "circle";
	}
};


